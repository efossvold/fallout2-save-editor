const toDict = (items: Item[]): Items =>
  items.reduce<Items>((acc, item) => {
    acc[item.id] = item
    return acc
  }, {})

export const ITEMS = {
  ...toDict([...WEAPONS]),
  ...toDict([...ARMOR]),
  ...toDict([...AMMO]),
  ...toDict([...DRUGS]),
  ...toDict([...MISC]),
}

export const getCategory = (item: Item): ItemCategory => {
  if (WEAPONS.includes(item)) {
    return ItemCategory.WEAPON
  }
  if (ARMOR.includes(item)) {
    return ItemCategory.ARMOR
  }
  if (AMMO.includes(item)) {
    return ItemCategory.AMMO
  }
  if (DRUGS.includes(item)) {
    return ItemCategory.DRUG
  }
  if (MISC.includes(item)) {
    return ItemCategory.MISC
  }
  throw Error(`Unknown category for item ID (${item.id})`)
}
